package TP2;

public class Adresse {

        public String voie;
        public String rue;
        public String cp;
        public String ville;
        public String pays;

        public Adresse(String voie, String rue, String cp, String ville, String pays) {
                this.voie = voie;
                this.rue = rue;
                this.cp = cp;
                this.ville = ville;
                this.pays = pays;
        }
}
